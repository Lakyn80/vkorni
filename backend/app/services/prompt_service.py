"""
prompt_service.py
-----------------
Deterministic prompt building for safe memorial biographies.
"""

ANGLES: list[dict] = [
    {
        "id": "factual_memorial",
        "directive": (
            "Напиши спокойную, уважительную и документальную биографическую заметку "
            "на русском языке только по подтвержденным фактам из входных данных."
        ),
        "tone": "спокойный, уважительный, документальный",
    },
]


def pick_angle(exclude_ids: list[str] | None = None) -> dict:
    """Return the single deterministic biography angle."""
    return ANGLES[0]


def build_system_prompt(angle: dict, style: str | None = None) -> str:
    prompt = (
        "Ты пишешь краткие мемориальные биографии для Vkorni.com.\n"
        "Текст должен быть только на русском языке.\n\n"
        "Строгие правила:\n"
        "1. Используй только факты, явно присутствующие во входных данных.\n"
        "2. Ничего не выдумывай и не достраивай по вероятности.\n"
        "3. Запрещено придумывать семью, место рождения, характер, цитаты, болезнь, "
        "причину смерти, военную службу, награды, должности, карьерные детали, "
        "даты, географию и любые иные сведения, которых нет во входных данных.\n"
        "4. Если данных мало, напиши короткую, но аккуратно оформленную биографическую заметку "
        "только по доступным фактам.\n"
        "5. Если какое-либо поле пустое или отсутствует, просто пропусти его.\n"
        "6. Не упоминай, что ты модель, нейросеть или ИИ.\n"
        "7. Не используй Markdown-решётки, списки или декоративные выдуманные детали.\n"
        "8. Соблюдай спокойный, уважительный, документальный мемориальный тон.\n"
        "9. SEO-лексика допустима только естественно и только если она прямо "
        "поддерживается входными данными.\n"
        "10. Верни только готовый plain text биографии без пояснений.\n"
        "11. Формат должен быть аккуратным и памятным: можно использовать короткие "
        "эмодзи-заголовки разделов, но только в безопасной структуре.\n\n"
        "Безопасная структура:\n"
        "• Первая строка: полное имя, если оно известно.\n"
        "• Вторая строка: даты жизни, если они известны.\n"
        "• Затем 2-4 коротких раздела plain text с заголовками вида "
        "«🕯️ Биография», «📚 Путь и дело», «🏅 Признание», «🕊️ Память».\n"
        "• Разделы без фактов пропускай.\n"
        "• Не добавляй раздел, если для него нет подтвержденных данных.\n\n"
        f"Тон текста: {angle['tone']}.\n"
        f"Базовая директива: {angle['directive']}\n"
    )

    if style:
        prompt += (
            "\nСтилистический ориентир можно учитывать только на уровне ритма и "
            "интонации, но нельзя переносить из него никакие факты:\n"
            + style[:1500]
        )

    return prompt


def build_user_message(context: str, angle: dict) -> str:
    return (
        "Подготовь биографию строго по данным ниже. Если подтвержденных сведений "
        "мало, верни короткий нейтральный текст без домыслов.\n\n"
        "Подтвержденные данные:\n"
        f"{context}"
    )
